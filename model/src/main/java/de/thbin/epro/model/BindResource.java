package de.thbin.epro.model;

public class BindResource {
    String app_guid;
    String route;

    public String getApp_guid() {
        return app_guid;
    }

    public void setApp_guid(String app_guid) {
        this.app_guid = app_guid;
    }

    public String getRoute() {
        return route;
    }

    public void setRoute(String route) {
        this.route = route;
    }
}
